#include "../includes/leaks.h"

int	ft_toupper(int c)
{
	if (c < 0 || c > 127)
		return (c);
	if ((unsigned char) c >= 'a' && (unsigned char) c <= 'z')
		return (c - 32);
	return (c);
}
